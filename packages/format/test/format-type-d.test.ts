import { describe, expect, test } from 'bun:test'
import { format } from '../src/index.ts'

describe('format type d', () => {
  test('format("d") can zero fill', () => {
    const f = format('08d')
    expect(f(0)).toBe('00000000')
    expect(f(42)).toBe('00000042')
    expect(f(42000000)).toBe('42000000')
    expect(f(420000000)).toBe('420000000')
    expect(f(-4)).toBe('\u22120000004')
    expect(f(-42)).toBe('\u22120000042')
    expect(f(-4200000)).toBe('\u22124200000')
    expect(f(-42000000)).toBe('\u221242000000')
  })

  test('format("d") can space fill', () => {
    const f = format('8d')
    expect(f(0)).toBe('       0')
    expect(f(42)).toBe('      42')
    expect(f(42000000)).toBe('42000000')
    expect(f(420000000)).toBe('420000000')
    expect(f(-4)).toBe('      \u22124')
    expect(f(-42)).toBe('     \u221242')
    expect(f(-4200000)).toBe('\u22124200000')
    expect(f(-42000000)).toBe('\u221242000000')
  })

  test('format("d") can underscore fill', () => {
    const f = format('_>8d')
    expect(f(0)).toBe('_______0')
    expect(f(42)).toBe('______42')
    expect(f(42000000)).toBe('42000000')
    expect(f(420000000)).toBe('420000000')
    expect(f(-4)).toBe('______\u22124')
    expect(f(-42)).toBe('_____\u221242')
    expect(f(-4200000)).toBe('\u22124200000')
    expect(f(-42000000)).toBe('\u221242000000')
  })

  test('format("d") can zero fill with sign and group', () => {
    const f = format('+08,d')
    expect(f(0)).toBe('+0,000,000')
    expect(f(42)).toBe('+0,000,042')
    expect(f(42000000)).toBe('+42,000,000')
    expect(f(420000000)).toBe('+420,000,000')
    expect(f(-4)).toBe('\u22120,000,004')
    expect(f(-42)).toBe('\u22120,000,042')
    expect(f(-4200000)).toBe('\u22124,200,000')
    expect(f(-42000000)).toBe('\u221242,000,000')
  })

  test('format("d") always uses zero precision', () => {
    const f = format('.2d')
    expect(f(0)).toBe('0')
    expect(f(42)).toBe('42')
    expect(f(-4.2)).toBe('\u22124')
  })

  test('format("d") rounds non-integers', () => {
    const f = format('d')
    expect(f(4.2)).toBe('4')
  })

  test('format(",d") can group thousands', () => {
    const f = format(',d')
    expect(f(0)).toBe('0')
    expect(f(42)).toBe('42')
    expect(f(42000000)).toBe('42,000,000')
    expect(f(420000000)).toBe('420,000,000')
    expect(f(-4)).toBe('\u22124')
    expect(f(-42)).toBe('\u221242')
    expect(f(-4200000)).toBe('\u22124,200,000')
    expect(f(-42000000)).toBe('\u221242,000,000')
    expect(f(1e21)).toBe('1,000,000,000,000,000,000,000')
    expect(f(1.3e27)).toBe('1,300,000,000,000,000,000,000,000,000')
    expect(f(1.3e107)).toBe('130,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000')
  })

  test('format("0,d") can group thousands and zero fill', () => {
    expect(format('01,d')(0)).toBe('0')
    expect(format('01,d')(0)).toBe('0')
    expect(format('02,d')(0)).toBe('00')
    expect(format('03,d')(0)).toBe('000')
    expect(format('04,d')(0)).toBe('0,000')
    expect(format('05,d')(0)).toBe('0,000')
    expect(format('06,d')(0)).toBe('00,000')
    expect(format('08,d')(0)).toBe('0,000,000')
    expect(format('013,d')(0)).toBe('0,000,000,000')
    expect(format('021,d')(0)).toBe('0,000,000,000,000,000')
    expect(format('013,d')(-42000000)).toBe('\u22120,042,000,000')
    expect(format('012,d')(1e21)).toBe('1,000,000,000,000,000,000,000')
    expect(format('013,d')(1e21)).toBe('1,000,000,000,000,000,000,000')
    expect(format('014,d')(1e21)).toBe('1,000,000,000,000,000,000,000')
    expect(format('015,d')(1e21)).toBe('1,000,000,000,000,000,000,000')
  })

  test('format("0,d") can group thousands and zero fill with overflow', () => {
    expect(format('01,d')(1)).toBe('1')
    expect(format('01,d')(1)).toBe('1')
    expect(format('02,d')(12)).toBe('12')
    expect(format('03,d')(123)).toBe('123')
    expect(format('05,d')(12345)).toBe('12,345')
    expect(format('08,d')(12345678)).toBe('12,345,678')
    expect(format('013,d')(1234567890123)).toBe('1,234,567,890,123')
  })

  test('format(",d") can group thousands and space fill', () => {
    expect(format('1,d')(0)).toBe('0')
    expect(format('1,d')(0)).toBe('0')
    expect(format('2,d')(0)).toBe(' 0')
    expect(format('3,d')(0)).toBe('  0')
    expect(format('5,d')(0)).toBe('    0')
    expect(format('8,d')(0)).toBe('       0')
    expect(format('13,d')(0)).toBe('            0')
    expect(format('21,d')(0)).toBe('                    0')
  })

  test('format(",d") can group thousands and space fill with overflow', () => {
    expect(format('1,d')(1)).toBe('1')
    expect(format('1,d')(1)).toBe('1')
    expect(format('2,d')(12)).toBe('12')
    expect(format('3,d')(123)).toBe('123')
    expect(format('5,d')(12345)).toBe('12,345')
    expect(format('8,d')(12345678)).toBe('12,345,678')
    expect(format('13,d')(1234567890123)).toBe('1,234,567,890,123')
  })

  test('format("<d") align left', () => {
    expect(format('<1,d')(0)).toBe('0')
    expect(format('<1,d')(0)).toBe('0')
    expect(format('<2,d')(0)).toBe('0 ')
    expect(format('<3,d')(0)).toBe('0  ')
    expect(format('<5,d')(0)).toBe('0    ')
    expect(format('<8,d')(0)).toBe('0       ')
    expect(format('<13,d')(0)).toBe('0            ')
    expect(format('<21,d')(0)).toBe('0                    ')
  })

  test('format(">d") align right', () => {
    expect(format('>1,d')(0)).toBe('0')
    expect(format('>1,d')(0)).toBe('0')
    expect(format('>2,d')(0)).toBe(' 0')
    expect(format('>3,d')(0)).toBe('  0')
    expect(format('>5,d')(0)).toBe('    0')
    expect(format('>8,d')(0)).toBe('       0')
    expect(format('>13,d')(0)).toBe('            0')
    expect(format('>21,d')(0)).toBe('                    0')
    expect(format('>21,d')(1000)).toBe('                1,000')
    expect(format('>21,d')(1e21)).toBe('1,000,000,000,000,000,000,000')
  })

  test('format("^d") align center', () => {
    expect(format('^1,d')(0)).toBe('0')
    expect(format('^1,d')(0)).toBe('0')
    expect(format('^2,d')(0)).toBe('0 ')
    expect(format('^3,d')(0)).toBe(' 0 ')
    expect(format('^5,d')(0)).toBe('  0  ')
    expect(format('^8,d')(0)).toBe('   0    ')
    expect(format('^13,d')(0)).toBe('      0      ')
    expect(format('^21,d')(0)).toBe('          0          ')
    expect(format('^21,d')(1000)).toBe('        1,000        ')
    expect(format('^21,d')(1e21)).toBe('1,000,000,000,000,000,000,000')
  })

  test('format("=+,d") pad after sign', () => {
    expect(format('=+1,d')(0)).toBe('+0')
    expect(format('=+1,d')(0)).toBe('+0')
    expect(format('=+2,d')(0)).toBe('+0')
    expect(format('=+3,d')(0)).toBe('+ 0')
    expect(format('=+5,d')(0)).toBe('+   0')
    expect(format('=+8,d')(0)).toBe('+      0')
    expect(format('=+13,d')(0)).toBe('+           0')
    expect(format('=+21,d')(0)).toBe('+                   0')
    expect(format('=+21,d')(1e21)).toBe('+1,000,000,000,000,000,000,000')
  })

  test('format("=+$,d") pad after sign with currency', () => {
    expect(format('=+$1,d')(0)).toBe('+$0')
    expect(format('=+$1,d')(0)).toBe('+$0')
    expect(format('=+$2,d')(0)).toBe('+$0')
    expect(format('=+$3,d')(0)).toBe('+$0')
    expect(format('=+$5,d')(0)).toBe('+$  0')
    expect(format('=+$8,d')(0)).toBe('+$     0')
    expect(format('=+$13,d')(0)).toBe('+$          0')
    expect(format('=+$21,d')(0)).toBe('+$                  0')
    expect(format('=+$21,d')(1e21)).toBe('+$1,000,000,000,000,000,000,000')
  })

  test('format(" ,d") a space can denote positive numbers', () => {
    expect(format(' 1,d')(-1)).toBe('\u22121')
    expect(format(' 1,d')(0)).toBe(' 0')
    expect(format(' 2,d')(0)).toBe(' 0')
    expect(format(' 3,d')(0)).toBe('  0')
    expect(format(' 5,d')(0)).toBe('    0')
    expect(format(' 8,d')(0)).toBe('       0')
    expect(format(' 13,d')(0)).toBe('            0')
    expect(format(' 21,d')(0)).toBe('                    0')
    expect(format(' 21,d')(1e21)).toBe(' 1,000,000,000,000,000,000,000')
  })

  test('format("-,d") explicitly only use a sign for negative numbers', () => {
    expect(format('-1,d')(-1)).toBe('\u22121')
    expect(format('-1,d')(0)).toBe('0')
    expect(format('-2,d')(0)).toBe(' 0')
    expect(format('-3,d')(0)).toBe('  0')
    expect(format('-5,d')(0)).toBe('    0')
    expect(format('-8,d')(0)).toBe('       0')
    expect(format('-13,d')(0)).toBe('            0')
    expect(format('-21,d')(0)).toBe('                    0')
  })

  test('format("d") can format negative zero as zero', () => {
    expect(format('1d')(-0)).toBe('0')
    expect(format('1d')(-1e-12)).toBe('0')
  })
})
