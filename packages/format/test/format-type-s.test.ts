import { describe, expect, test } from 'bun:test'
import { format } from '../src/index.ts'

describe('format type s', () => {
  test('format("s") outputs SI-prefix notation with default precision 6', () => {
    const f = format('s')
    expect(f(0)).toBe('0.00000')
    expect(f(1)).toBe('1.00000')
    expect(f(10)).toBe('10.0000')
    expect(f(100)).toBe('100.000')
    expect(f(999.5)).toBe('999.500')
    expect(f(999500)).toBe('999.500k')
    expect(f(1000)).toBe('1.00000k')
    expect(f(100)).toBe('100.000')
    expect(f(1400)).toBe('1.40000k')
    expect(f(1500.5)).toBe('1.50050k')
    expect(f(.00001)).toBe('10.0000\u00b5')
    expect(f(.000001)).toBe('1.00000\u00b5')
  })

  test('format("s") does not get confused by NaN, Infinity, etc.', () => {
    const f = format('s')
    expect(f(999500)).toBe('999.500k')
    expect(f(Infinity)).toBe('Infinity')
    expect(f(NaN)).toBe('NaN')
  })

  test('format("[.precision]s") outputs SI-prefix notation with precision significant digits', () => {
    const f1 = format('.3s')
    expect(f1(0)).toBe('0.00')
    expect(f1(1)).toBe('1.00')
    expect(f1(10)).toBe('10.0')
    expect(f1(100)).toBe('100')
    expect(f1(999.5)).toBe('1.00k')
    expect(f1(999500)).toBe('1.00M')
    expect(f1(1000)).toBe('1.00k')
    expect(f1(1500.5)).toBe('1.50k')
    expect(f1(145500000)).toBe('146M')
    expect(f1(145999999.99999347)).toBe('146M')
    expect(f1(1e26)).toBe('100Y')
    expect(f1(.000001)).toBe('1.00\u00b5')
    expect(f1(.009995)).toBe('10.0m')
    const f2 = format('.4s')
    expect(f2(999.5)).toBe('999.5')
    expect(f2(999500)).toBe('999.5k')
    expect(f2(.009995)).toBe('9.995m')
  })

  test('format("s") formats numbers smaller than 1e-24 with yocto', () => {
    const f = format('.8s')
    expect(f(1.29e-30)).toBe('0.0000013y')
    expect(f(1.29e-29)).toBe('0.0000129y')
    expect(f(1.29e-28)).toBe('0.0001290y')
    expect(f(1.29e-27)).toBe('0.0012900y')
    expect(f(1.29e-26)).toBe('0.0129000y')
    expect(f(1.29e-25)).toBe('0.1290000y')
    expect(f(1.29e-24)).toBe('1.2900000y')
    expect(f(1.29e-23)).toBe('12.900000y')
    expect(f(1.29e-22)).toBe('129.00000y')
    expect(f(1.29e-21)).toBe('1.2900000z')
    expect(f(-1.29e-30)).toBe('\u22120.0000013y')
    expect(f(-1.29e-29)).toBe('\u22120.0000129y')
    expect(f(-1.29e-28)).toBe('\u22120.0001290y')
    expect(f(-1.29e-27)).toBe('\u22120.0012900y')
    expect(f(-1.29e-26)).toBe('\u22120.0129000y')
    expect(f(-1.29e-25)).toBe('\u22120.1290000y')
    expect(f(-1.29e-24)).toBe('\u22121.2900000y')
    expect(f(-1.29e-23)).toBe('\u221212.900000y')
    expect(f(-1.29e-22)).toBe('\u2212129.00000y')
    expect(f(-1.29e-21)).toBe('\u22121.2900000z')
  })

  test('format("s") formats numbers larger than 1e24 with yotta', () => {
    const f = format('.8s')
    expect(f(1.23e+21)).toBe('1.2300000Z')
    expect(f(1.23e+22)).toBe('12.300000Z')
    expect(f(1.23e+23)).toBe('123.00000Z')
    expect(f(1.23e+24)).toBe('1.2300000Y')
    expect(f(1.23e+25)).toBe('12.300000Y')
    expect(f(1.23e+26)).toBe('123.00000Y')
    expect(f(1.23e+27)).toBe('1230.0000Y')
    expect(f(1.23e+28)).toBe('12300.000Y')
    expect(f(1.23e+29)).toBe('123000.00Y')
    expect(f(1.23e+30)).toBe('1230000.0Y')
    expect(f(-1.23e+21)).toBe('\u22121.2300000Z')
    expect(f(-1.23e+22)).toBe('\u221212.300000Z')
    expect(f(-1.23e+23)).toBe('\u2212123.00000Z')
    expect(f(-1.23e+24)).toBe('\u22121.2300000Y')
    expect(f(-1.23e+25)).toBe('\u221212.300000Y')
    expect(f(-1.23e+26)).toBe('\u2212123.00000Y')
    expect(f(-1.23e+27)).toBe('\u22121230.0000Y')
    expect(f(-1.23e+28)).toBe('\u221212300.000Y')
    expect(f(-1.23e+29)).toBe('\u2212123000.00Y')
    expect(f(-1.23e+30)).toBe('\u22121230000.0Y')
  })

  test('format("$s") outputs SI-prefix notation with a currency symbol', () => {
    const f1 = format('$.2s')
    expect(f1(0)).toBe('$0.0')
    expect(f1(2.5e5)).toBe('$250k')
    expect(f1(-2.5e8)).toBe('\u2212$250M')
    expect(f1(2.5e11)).toBe('$250G')
    const f2 = format('$.3s')
    expect(f2(0)).toBe('$0.00')
    expect(f2(1)).toBe('$1.00')
    expect(f2(10)).toBe('$10.0')
    expect(f2(100)).toBe('$100')
    expect(f2(999.5)).toBe('$1.00k')
    expect(f2(999500)).toBe('$1.00M')
    expect(f2(1000)).toBe('$1.00k')
    expect(f2(1500.5)).toBe('$1.50k')
    expect(f2(145500000)).toBe('$146M')
    expect(f2(145999999.9999347)).toBe('$146M')
    expect(f2(1e26)).toBe('$100Y')
    expect(f2(.000001)).toBe('$1.00\u00b5')
    expect(f2(.009995)).toBe('$10.0m')
    const f3 = format('$.4s')
    expect(f3(999.5)).toBe('$999.5')
    expect(f3(999500)).toBe('$999.5k')
    expect(f3(.009995)).toBe('$9.995m')
  })

  test('format("s") SI-prefix notation precision is consistent for small and large numbers', () => {
    const f1 = format('.0s')
    expect(f1(1e-5)).toBe('10\u00b5')
    expect(f1(1e-4)).toBe('100\u00b5')
    expect(f1(1e-3)).toBe('1m')
    expect(f1(1e-2)).toBe('10m')
    expect(f1(1e-1)).toBe('100m')
    expect(f1(1e+0)).toBe('1')
    expect(f1(1e+1)).toBe('10')
    expect(f1(1e+2)).toBe('100')
    expect(f1(1e+3)).toBe('1k')
    expect(f1(1e+4)).toBe('10k')
    expect(f1(1e+5)).toBe('100k')
    const f2 = format('.4s')
    expect(f2(1e-5)).toBe('10.00\u00b5')
    expect(f2(1e-4)).toBe('100.0\u00b5')
    expect(f2(1e-3)).toBe('1.000m')
    expect(f2(1e-2)).toBe('10.00m')
    expect(f2(1e-1)).toBe('100.0m')
    expect(f2(1e+0)).toBe('1.000')
    expect(f2(1e+1)).toBe('10.00')
    expect(f2(1e+2)).toBe('100.0')
    expect(f2(1e+3)).toBe('1.000k')
    expect(f2(1e+4)).toBe('10.00k')
    expect(f2(1e+5)).toBe('100.0k')
  })

  test('format("0[width],s") will group thousands due to zero fill', () => {
    const f = format('020,s')
    expect(f(42)).toBe('000,000,000,042.0000')
    expect(f(42e12)).toBe('00,000,000,042.0000T')
  })

  test('format(",s") will group thousands for very large numbers', () => {
    const f = format(',s')
    expect(f(42e30)).toBe('42,000,000Y')
  })
})
