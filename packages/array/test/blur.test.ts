it('blur2(data, rx, 0) does horizontal blurring', () => {
  expect(blur2(copy2(unit), 0, 0).data).toEqual(unit.data)
})

it('blur2(data, 0, ry) does vertical blurring', () => {
  expect(blur2(copy2(unit), 0, 0).data).toEqual(unit.data)
})
